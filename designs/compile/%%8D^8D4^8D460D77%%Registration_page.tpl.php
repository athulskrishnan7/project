    <h3>What is GST?</h3>
    <p>
        GST or Goods and Service Tax is is one indirect tax for the whole nation. <br>
        It is the resultant tax after subsuming major Central and State indirect taxes.<br>
        It is used for replacing various taxes that were imposed indirectly such as excise duty, VAT, services taxes, and others. <br>

    </p>


    <h3>How our system works?</h3>
    <p>
        We are using a four-user type web-based system: Main admin, GST controller, Shop/institutions that pay GST and public (end user). <br>
        Our hierarchy based system allows the Main admin to handle the work load by adding GST controllers who are responsible for the complaint handling, tax auditing and product category and sub-category addition.<br>
        The shop/institutions who use our system to calculate and pay GST, report tax, complaint and product addition and updation.<br>
        The user can send complaints related to the GST directly to a GST counsellor who then verifies it and passes to the Main admin if the complaint is valid.<br>



    </p>

